package Ex61Collection.Ex1ArrayList.Ex6ArrayListEmp;

public class Emp {
    private int age;
    private String name;
    private double sal;


    public Emp(int age, String name, double sal) {
        this.age = age;
        this.name = name;
        this.sal = sal;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public double getSal() {
        return sal;
    }
}
